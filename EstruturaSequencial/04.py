""" Faça um programa que peça as 4 notas bimestrais e mostre a media """

n1 = int(input("Entre com a nota do primeiro bimestre: "))
n2 = int(input("Entre com a nota do segundo bimestre: "))
n3 = int(input("Entre com a nota do terceiro bimestre: "))
n4 = int(input("Entre com a nota do quarto bimestre "))




media = (n1 + n2 + n3 + n4) / 4

print(f'A media é: {media}')